  export const useTranslation = (language) => {
    const t = (key, params = {}) => {
      const keys = key.split('.')
      let value = translations[language]
      
      for (const k of keys) {
        if (value && typeof value === 'object') {
          value = value[k]
        } else {
          return key
        }
      }
      
      if (typeof value === 'string') {
        return Object.entries(params).reduce(
          (str, [k, v]) => str.replace(`{${k}}`, v),
          value
        )
      }
      
      return value || key
    }
    
    return { t }
  }